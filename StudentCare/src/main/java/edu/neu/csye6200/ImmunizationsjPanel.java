    public void populateBodyVaccinations() {
       // vaccineController.removeAll();
        List<ImmunizationClass> vaccinations = vaccineController.getAllVaccines();

        if (vaccinations.size()<=1&&vaccinations.get(0).getVaccine1Dose1()==null) {
            vaccinations.remove(0);
            vaccineController.addTestData();
        } else {
            DefaultTableModel model = (DefaultTableModel) tbVaccine.getModel();

            if (model.getRowCount() == 0) {
                vaccinations.forEach(t -> {
                    model.addRow(new Object[]{t.getPersonID(), t.readVaccine(), t.getVaccine1Dose1(), t.getVaccine1Dose2(), t.readVaccine2(), t.getVaccine2Dose1()});
                });
            }

        }
    }

