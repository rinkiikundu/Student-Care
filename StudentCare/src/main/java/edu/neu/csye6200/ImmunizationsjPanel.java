    private void btnAddRecordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddRecordActionPerformed
        // TODO add your handling code here:
        String id = txtPersonId.getText();
        String vaccine1 = txtVaccine1.getText();
        String v1t1 = txtV1Dose1.getText();
        String v1t2 = txtV1Dose2.getText();
        String vaccine2 = txtVaccine2.getText();
        String v2t1 = txtV2Dose1.getText();

        if (id.isEmpty() || vaccine1.isEmpty() || v1t1.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Atleast 1 vaccination info", "Try Again", JOptionPane.ERROR_MESSAGE);
        } else {
            DefaultTableModel model = (DefaultTableModel) tbVaccine.getModel();
            model.addRow(new Object[]{id, vaccine1, v1t1, v1t2, vaccine2, v2t1});
        }

        txtPersonId.setText("");
        txtVaccine1.setText("");
        txtV1Dose1.setText("");
        txtV1Dose2.setText("");
        txtVaccine2.setText("");
        txtV2Dose1.setText("");

        String csv = id + "," + vaccine1 + "," + v1t1 + "," + v1t2 + "," + vaccine2 + "," + v2t1;
        System.err.println(csv + "is added to vaccine db");

        vaccineController.addVaccine(csv);
    }//GEN-LAST:event_btnAddRecordActionPerformed

